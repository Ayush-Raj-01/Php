<?php

$x = 10;
$y = 20;

echo "x + y: " . ($x + $y) . "\n";
echo "x - y: " . ($x - $y) . "\n";
echo "x / y: " . ($x / $y) . "\n";
echo "x * y: " . ($x * $y) . "\n";

// increment and decrement operator 

echo "value of X before incrementation : $x \n";
echo "x++: " . $x++ . "\n";
echo "x: " . $x . "\n";

echo "++x: " . ++$x . "\n";
echo "x: " . $x . "\n";

echo "value of y before decrementation : $y \n";
echo "y--: " . $y-- . "\n";
echo "y: " . $y . "\n";

echo "--y: " . --$y . "\n";
echo "y: " . $y . "\n";
?>